# I Speak TXTMSG
while 1:
    txtmsg = input()
    if txtmsg == 'CU':
        print("see you")
    elif txtmsg == ':-)':
        print("I’m happy")
    elif txtmsg == ':-(':
        print("I’m unhappy")
    elif txtmsg == ';-)':
        print("wink")
    elif txtmsg == ':-P':
        print("stick out my tongue")
    elif txtmsg == '(~.~)':
        print("sleepy")
    elif txtmsg == 'TA':
        print("totally awesome")
    elif txtmsg == 'CCC':
        print("Canadian Computing Competition")
    elif txtmsg == 'CUZ':
        print("because")
    elif txtmsg == 'TY':
        print("thank-you")
    elif txtmsg == 'YW':
        print("you’re welcome")
    elif txtmsg == 'TTYL':
        print("talk to you later")
        break
    else:
        print(txtmsg)